    private void FinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinalizarActionPerformed
        // TODO add your handling code here:
        NegociosCompra ncom = new NegociosCompra();
        String c = jComboCliente.getSelectedItem().toString().trim();
        NegociosCliente nc = new NegociosCliente();
        Cliente cliente = nc.ProcurarCliente(c);
        Timestamp data = new Timestamp(System.currentTimeMillis());
        System.out.println(data);
        boolean isEntrega = jCheckBox2.isSelected();
        List<Integer> lista_prod = new ArrayList<Integer>();
        List<Integer> quantidades= new ArrayList<Integer>();
        List<Integer> listNovasQuants = new ArrayList<Integer>();
        NegociosProduto np = new NegociosProduto();
        int ab=0;
        for(int x=0;x<jTable1.getRowCount();x++){
            lista_prod.add(Integer.parseInt(jTable1.getValueAt(x, 0).toString()));
            quantidades.add(Integer.parseInt(jTable1.getValueAt(x, 3).toString()));
            Produto p = np.ProcurarProduto(jTable1.getValueAt(x, 1).toString());
            if(p.getQuantidade() < quantidades.get(x).intValue()){
                JOptionPane.showMessageDialog(rootPane, "Produto "+jTable1.getValueAt(x, 1).toString()+"\n Quantidade Solicitada: "+ quantidades.get(x).intValue()+"Quantidade em Estoque: "+p.getQuantidade());
                ab=1;
            }else{
                listNovasQuants.add(p.getQuantidade() - quantidades.get(x).intValue());
            }
        }
        if(ab==0){
            int codigo = ncom.salvar(cliente.getCodigo(), data, isEntrega, total, lista_prod, quantidades);
            for(int x=0;x<lista_prod.size();x++){
                np.UpdateProduto(listNovasQuants.get(x).intValue(), lista_prod.get(x).intValue());
            }
            JOptionPane.showMessageDialog(rootPane, "Compra Realizada com Sucesso");
            if(isEntrega){
                try {
                    new TelaRelatorio().GerarEntrega(codigo);
                } catch (IOException ex) {
                    Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else if(ab==1){
            int y = jTable1.getRowCount();
            DefaultTableModel modeloTabela = (DefaultTableModel)jTable1.getModel();
            //modeloTabela.addRow(new String[ modeloTabela.getColumnCount() ]);
            for(int a = 0; a < y;a++){
                modeloTabela.removeRow(a);
            }
        }
    }//GEN-LAST:event_FinalizarActionPerformed

